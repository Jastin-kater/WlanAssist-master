package com.ronda.bluetoothassist.linesets;

public class MyBean {
    private String s;
    private float f;
    public MyBean(String s, float v) {
        this.s = s;
        this.f = v;
    }

    public float getF() {
        return f;
    }

    public void setF(float f) {
        this.f = f;
    }

    public String getS() {
        return s;
    }
}
